"""
Merges the Bitext-derived cases (billing/account/general) and the real
GitHub-issue-derived cases (technical) into the final versioned golden
dataset used by the eval pipeline in Phase 3.
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path

GOLDEN_DIR = Path(__file__).resolve().parent.parent / "golden_dataset"


def main() -> None:
    bitext_cases = json.loads((GOLDEN_DIR / "bitext_cases.json").read_text())
    github_cases = json.loads((GOLDEN_DIR / "github_cases.json").read_text())

    all_cases = bitext_cases + github_cases

    dataset = {
        "dataset_version": "v1",
        "created_at": datetime.now(timezone.utc).isoformat(),
        "description": (
            "Golden dataset for the email-classifier eval pipeline. "
            "billing/account/general cases sourced from the real Bitext customer "
            "support dataset (CDLA-Sharing-1.0), category-remapped and "
            "hand-summarized per intent. technical cases sourced from real, "
            "live GitHub issues on consumer desktop apps (Signal Desktop, "
            "GitHub Desktop), cleaned of template scaffolding and hand-summarized "
            "per issue. No case content was generated by an LLM."
        ),
        "sources": {
            "bitext": "https://github.com/bitext/customer-support-llm-chatbot-training-dataset (CDLA-Sharing-1.0)",
            "github_issues": "Public GitHub issue trackers, see per-case source_url",
        },
        "cases": all_cases,
    }

    out_path = GOLDEN_DIR / "golden_dataset_v1.json"
    out_path.write_text(json.dumps(dataset, indent=2, ensure_ascii=False), encoding="utf-8")

    print(f"Wrote {len(all_cases)} total cases to {out_path}")
    by_cat: dict[str, int] = {}
    by_diff: dict[str, int] = {}
    for c in all_cases:
        by_cat[c["expected_category"]] = by_cat.get(c["expected_category"], 0) + 1
        by_diff[c["expected_difficulty"]] = by_diff.get(c["expected_difficulty"], 0) + 1
    print("By category:", by_cat)
    print("By difficulty:", by_diff)


if __name__ == "__main__":
    main()
